package com.javarush.task.task23.task2312;


public class Mouse {
    private int x;
    private int y;
    
    public int getX() {
        return this.x;
    }
    
    public int getY() {
        return this.y;
    }
    
    public void setX(int x) {
        this.x = x;
    }
    
    public void setY(int y) {
        this.y = y;
    }
    
    public Mouse(int x, int y) {
        this.x = x;
        this.y = y;
    }
}
